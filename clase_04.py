# Operaciones aritméticas
# Suma
a = 10 
b = 3 
print("suma:", a + b)
# Resta
print("resta:", a - b)
# Multiplicación
print("multiplicacion:", a * b)
# División
print("division:", a / b) #devuelve un float    
# División entera
print("division entera:", a // b) #devuelve un int
# Módulo
print("modulo:", a % b) #devuelve el resto de la division
# Potencia
print("potencia:", a ** b) #devuelve a elevado a la b
# Precedencia de operadores: (), **, *, /, //, %, +, -
# Se pueden usar paréntesis para cambiar la precedencia
resultado = (a + b) * (a - b)
print("resultado:", resultado)

# en esta clase se vio el uso de operadores aritmeticos y la precedencia de operadores