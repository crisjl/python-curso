a = 10
b = 3
print(a > b) #mayor que
print(a < b) #menor que
print(a >= b) #mayor o igual que
print(a <= b) #menor o igual que
print(a == b) #igual que
print(a != b) #diferente que
#en esta clase se vio el uso de operadores de comparacion y operadores logicos
print(a > b and a != b) #operador and, devuelve True si ambas condiciones son True
print(a > b or a == b) #operador or, devuelve True si al menos una condicion es True
print(not(a > b)) #operador not, invierte el valor de la condicion          
